window.UI_LOCALE = localStorage.getItem("edu_locale") || "en";

window.setUILocale = function setUILocale(locale) {
  if (!window.UI_COPY[locale]) return;
  localStorage.setItem("edu_locale", locale);
  window.UI_LOCALE = locale;
};

window.getCopy = function getCopy(section) {
  const lang = window.UI_COPY[window.UI_LOCALE] || window.UI_COPY.en;
  return {
    ...(lang.common || {}),
    ...(lang[section] || {}),
  };
};
